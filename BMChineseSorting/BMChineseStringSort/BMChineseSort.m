//数组操作信号量
dispatch_semaphore_t semaphore;

@implementation BMChineseSort

+(NSString *)transformChinese:(NSString *)word{
    NSMutableString *pinyin = [word mutableCopy];
    CFStringTransform((__bridge CFMutableStringRef)pinyin, NULL, kCFStringTransformMandarinLatin, NO);
    CFStringTransform((__bridge CFMutableStringRef)pinyin, NULL, kCFStringTransformStripCombiningMarks, NO);
    return [pinyin uppercaseString];
}

#pragma mark ==============给 对象数组 根据某个属性 排序==================
+(void)sortWithArray:(NSArray*)objectArray key:(NSString *)key finish:(void (^)(bool isSuccess, NSMutableArray<NSString*> *sectionTitleArr, NSMutableArray<NSMutableArray*>* sortedObjArr))finish{
    if (!objectArray || objectArray.count == 0) {
        finish(YES,@[].mutableCopy,@[].mutableCopy);
        return;
    }
    //非法 属性名 检测
    BOOL containKey = NO;
    NSObject *obj = objectArray.firstObject;
    if (key == nil) {
        if (![obj isKindOfClass:NSString.class]) {
            [BMChineseSort logMsg:@"数组内元素不是字符串类型,如果是对象类型，请传key"];
            finish(NO,nil,nil);
            return;
        }
        containKey = YES;
    }else{
        NSObject *obj = objectArray.firstObject;
        unsigned int outCount, i;
        Ivar *ivars = class_copyIvarList(obj.class, &outCount);
        for (i = 0; i < outCount; i++) {
            Ivar property = ivars[i];
            NSString *keyName = [NSString stringWithCString:ivar_getName(property) encoding:NSUTF8StringEncoding];
            keyName = [keyName stringByReplacingOccurrencesOfString:@"_" withString:@""];
            if ([keyName isEqualToString:key]) {
                containKey = YES;
            }
        }
    }
    if (!containKey) {
        [BMChineseSort logMsg:@"数组内元素未包含指定属性"];
        finish(NO,nil,nil);
        return;
    }
    CFAbsoluteTime start = CFAbsoluteTimeGetCurrent();

    semaphore = dispatch_semaphore_create(1);

    //异步执行
    dispatch_async(dispatch_get_global_queue(0, 0), ^{
        //将数据 转换为 BMChineseSortModel
        NSMutableArray *sortModelArray = [NSMutableArray arrayWithCapacity:0];
        [objectArray enumerateObjectsWithOptions:NSEnumerationConcurrent usingBlock:^(id  _Nonnull obj, NSUInteger idx, BOOL * _Nonnull stop) {
            BMChineseSortModel *model = [self getModelWithObj:obj key:key];
            if (model) {
                //对 数组的插入操作 上锁
                dispatch_semaphore_wait(semaphore, DISPATCH_TIME_FOREVER);
                    [sortModelArray addObject:model];
                dispatch_semaphore_signal(semaphore);
            }
        }];


        CFAbsoluteTime state1 = CFAbsoluteTimeGetCurrent();
        [BMChineseSort logMsg:@""];
        [BMChineseSort logMsg:[NSString stringWithFormat:@"转拼音用时：\t %f s", (state1-start)]];

        //根据BMChineseSortModel的pinYin字段 升序 排列
        NSSortDescriptor *sortDescriptor = [NSSortDescriptor sortDescriptorWithKey:@"pinYin" ascending:YES];
        [sortModelArray sortUsingDescriptors:@[sortDescriptor]];

        //打印 排序用时
        CFAbsoluteTime state2 = CFAbsoluteTimeGetCurrent();
        [BMChineseSort logMsg:[NSString stringWithFormat:@"排序用时：\t %f s", (state2-state1)]];

        //分组
        NSMutableArray<NSString *> *sectionTitleArr = [NSMutableArray array];
        NSMutableArray<NSMutableArray *> *sortedObjArr = [NSMutableArray array];
        NSMutableArray *newSection = [NSMutableArray array];
        NSString *lastTitle;
        //拼音分组 稳定的分组排序 所以组内不需要再排
        for (BMChineseSortModel* object in sortModelArray) {
            NSString *firstLetter = [object.pinYin substringToIndex:1];
            id obj = object.object;
            //不同
            if(![lastTitle isEqualToString:firstLetter]){
                [sectionTitleArr addObject:firstLetter];
                //分组
                newSection = [NSMutableArray array];
                [sortedObjArr addObject:newSection];
                [newSection  addObject:obj];
                //用于下一次比较
                lastTitle = firstLetter;
            }else{//相同
                [newSection  addObject:obj];
            }
        }
        //打印 总用时
        CFAbsoluteTime state3 = CFAbsoluteTimeGetCurrent();
        [BMChineseSort logMsg:[NSString stringWithFormat:@"分组用时：\t %f s", (state3-state2)]];
        [BMChineseSort logMsg:[NSString stringWithFormat:@"BMChineseSort 排序总计用时：\t %f s", (state3-start)]];

        //回主线程
        dispatch_async(dispatch_get_main_queue(), ^{
            finish(YES,sectionTitleArr,sortedObjArr);
        });
    });
}

//将对象 转为 BMChineseSortModel
+(BMChineseSortModel*)getModelWithObj:(id)obj key:(NSString*)key{
    BMChineseSortModel *model = [[BMChineseSortModel alloc]init];
    model.object = obj;
    if (!key) {
        model.string = obj;
    }else{
        model.string = [obj valueForKeyPath:key];
    }
    //提出空白字符
    model.string = [model.string stringByTrimmingCharactersInSet:[NSCharacterSet whitespaceAndNewlineCharacterSet]];
    if(model.string == nil || model.string.length == 0){
        model.string = BMChineseSortSetting.share.specialCharSectionTitle;
    }else{
        //过滤 ignoreModelWithPrefix
        NSString *prefix = [model.string substringToIndex:1];
        if (![BMChineseSortSetting.share.ignoreModelWithPrefix containsString:prefix]) {
            //是否将字母与汉字拼音一起排序？？？？ （暂不考虑）
            //获取拼音首字母
            model.pinYin = [self getFirstLetter:model.string];
        }else{
            return nil;
        }
    }
    return model;
}


